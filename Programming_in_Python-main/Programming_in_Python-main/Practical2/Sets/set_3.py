#declaring set1
set1 = {1,2,3,4,5}

#declaring set2
set2 = {3,4,5,6,7,8}

#printing union
print("Union: ",set1 | set2)

#printing Intersection of set 1 and 2
print("Intersection: ",set1 & set2)

#printing Difference (set1 - set2)
print("Difference: ",set1-set2)

